#include "libft.h"

t_list	*ft_lstnew(void const *content, size_t content_size)
{
	t_list	*result;

	result = ft_memalloc(sizeof(t_list));
	result->content = (void*)content;
	result->content_size = content_size;
	result->next = NULL;
	return (result);
}
